// ============================================================
// Meal Library (Phase 5 — Meal Engine).
//
// Real, classified meals (NOT archetypes) the Meal Engine selects from
// when building the weekly plan. Each meal carries the classification
// signals the engine ranks on (cuisine / prepBand / budget /
// childFriendliness / tags) plus hard dietary flags used as filters.
//
// Cuisine / PrepBand / Budget reuse the vocabulary from mealArchetypes.ts
// so swipe signals (which key on Cuisine) line up with the library.
// ============================================================

import type { Cuisine, PrepBand, Budget } from './mealArchetypes';

export interface LibraryMeal {
  id: string;
  name: string;
  cuisine: Cuisine;
  prepBand: PrepBand;
  budget: Budget;
  childFriendliness: number; // 1 (adventurous) … 5 (very kid-friendly)
  tags: string[];
  ingredients: string[];
  // Hard dietary flags — used as filters, never as ranking signal.
  vegetarian: boolean;
  containsDairy: boolean;
  containsNuts: boolean;
  containsGluten: boolean;
  containsPork: boolean;
}

type Flags = Partial<
  Pick<
    LibraryMeal,
    | 'vegetarian'
    | 'containsDairy'
    | 'containsNuts'
    | 'containsGluten'
    | 'containsPork'
  >
>;

// Compact constructor so each meal is one readable line and the
// dietary flags default to false (only the true ones are listed).
function meal(
  id: string,
  name: string,
  cuisine: Cuisine,
  prepBand: PrepBand,
  budget: Budget,
  childFriendliness: number,
  tags: string[],
  ingredients: string[],
  flags: Flags = {},
): LibraryMeal {
  return {
    id,
    name,
    cuisine,
    prepBand,
    budget,
    childFriendliness,
    tags,
    ingredients,
    vegetarian: flags.vegetarian ?? false,
    containsDairy: flags.containsDairy ?? false,
    containsNuts: flags.containsNuts ?? false,
    containsGluten: flags.containsGluten ?? false,
    containsPork: flags.containsPork ?? false,
  };
}

